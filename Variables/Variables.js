// Variables allow you to assign a value

let color = "green"; // let keyword. "=" assighnemt operator.
console.log(color); // green
console.log(color + "!"); // green!

let number = 85;
console.log(number); // 85
console.log(number + 1); // 86
console.log(number); // 85
number = number + 1;
console.log(number); // 86
