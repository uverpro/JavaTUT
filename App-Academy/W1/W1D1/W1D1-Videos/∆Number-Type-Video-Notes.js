// Number Data Types

console.log(1 + 1); // 2
console.log(2.5 - 2); // .5
console.log(3 * 4); // 12
console.log(99 / 9); // 11

// Syntax:
// "+" = addition
// "-" = subtraction
// "*" = multiplication
// "/" = division

console.log(3 + 2 * 4); // 11
// REMEMBER: Order of operation. Multiplication takes precedent over addition
console.log((3 + 2) * 4) // 20


