// Number Data Types

console.log(1 + 1); // 2
console.log(2.5 - 2); // .5
console.log(3 * 4); // 12
console.log(99 / 9); // 11

// Syntax:
// "+" = addition
// "-" = subtraction
// "*" = multiplication
// "/" = division

console.log(3 + 2 * 4); // 11
// REMEMBER: Order of operation. Multiplication takes precedent over addition
console.log((3 + 2) * 4); // 20



// Modulo Operation (%)

console.log(20 % 5); // 0
console.log(21 % 5); // 1
console.log(22 % 5); // 2
console.log(23 % 5); // 3
console.log(24 % 5); // 4
console.log(25 % 5); // 0
console.log(26 % 5); // 1
