// The CONCAT() METHOD

// The concat() method concatenates the string arguments to the calling string
// and returns a new string.

const str1 = "Hello ";
const str2 = "World";
console.log(str1.concat(str2)); // => "Hello World"



let string1 = "Goodbye";
let string2 = "cruel world";
console.log(string1.concat(", ", string2)); // => "Goodbye, cruel world"
